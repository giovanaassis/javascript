/* ----- TYPES OF FUNCTIONS ----- */

// 1# -> Performing a task
function greet(name, lastName){
    console.log('Hello ' + name + ' ' + lastName);
}
greet('John', 'Smith');
// name = parameter
// john = argument

// 2# -> Calculating a value
function square(num){
    return num * num;
}
console.log(square(2));

