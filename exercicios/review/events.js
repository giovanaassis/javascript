/*
    onclick -> Disparado quando recebe um click.
    ondblclick -> Disparado quando clique duplo.
    onmouseover -> Disparado quando o mouse está sobre.
    onmouseout -> Disparado quando o mouse é movido para fora.
    onmousemove -> Disparado quando o mouse é movido no elemento.
    onmousedown -> Disparado quando o clique do mouse é pressionado.
    onmouseup -> Disparado quando o clique do mouse é liberado.
    onfocus -> Disparado quando o elemento recebe o foco.
    onchange -> Disparado quando existe uma mudança no conteúdo.
    onblur -> Disparado quando o elemento perde o foco.
    onkeydown -> Disparado quando uma tecla é pressionada.
    onkeypress -> Disparado quando uma tecla é pressionada e solta.
    onkeyup -> Disparado quando uma tecla é solta sobre um elemento.
    onload -> Disparado quando a página termina de carregar.
    onresize -> Disparado quando há um redimencionamento da janela.
*/

/* ----- EVENTOS DE TEMPO ----- */

/* 
    setTimeout(function, tempo em milisegundos)
    -> Executa uma função, depois de esperar um tempo específico.

    setInterval(function, milliseconds)
    -> É o mesmo, mas repete a execução da função em intervalos de tempo continuamente.

    Pra parar qualquer um dos 2 eventos usa o clearTimeout(var) e clearInterval(var).
*/